package ThenewBoston;

public class apples25MathClassMethods {
	public static void main(String[] args) {
		
		System.out.println(Math.ceil(7.4));    //up to number answer 8.0
		System.out.println(Math.floor(7.4));   //down to number answer 7.0
		System.out.println(Math.abs(-26.7));   //postivie number answer 26.7
		System.out.println(Math.max(8.6,5.2)); //gives max two numbers answer 8.6
		System.out.println(Math.min(8.6,5.2));  //gives min two numbers answer 5.2
		System.out.println(Math.pow(5,3));      //3 times five 5*5*5=125.0
		System.out.println(Math.sqrt(9));       //output is 3.0
	
	
	
	
	}
	

}
